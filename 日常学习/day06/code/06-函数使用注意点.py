# # 根据函数有没有返回值和参数 将函数定义为四类
# # 无参无返回值
# def 函数名():
#     pass
#
# # 无参有返回值
# def 函数名():
#     pass
#     return 数据
#
# # 有参数无返回值
# def 函数名(普通参数,不定长位置,缺省,不定长关键字):
#     pass
#
# # 有参数有返回值
# def 函数名(普通参数,不定长位置,缺省,不定长关键字):
#     return 数据值
#
# # 函数是否需要返回值: 在函数中获取的数据是否需要在函数外部使用 吐过需要就需要书写返回值
#
# # 函数是否书写参数 参数的目的是 使函数更加通用 能够解决 一系列的问题
