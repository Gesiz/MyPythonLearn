# 字符串: 使用引号引起来的内容就是字符串, 包括单引号 双引号 和三引号
# 单引号
name = 'isaac'
print(name, type(name))

# 双引号
name = "xw"
print(name, type(name))

# 三引号
info = '''good good study
day day up
'''
print(info, type(info))

info1 = """hello
python"""

print(info1, type(info1))

# 如果字符串本身包含引号,
# info = "I'm isaac"
info = 'I\'m isaac'
print(info)

